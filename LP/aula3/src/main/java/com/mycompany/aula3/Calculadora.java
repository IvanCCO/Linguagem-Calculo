public class Calculadora {
    
    double somar(double num1,
            double num2, double num3){
        
        return num1 + num2 + num3;
    }
    
    Integer somar(Integer num1,
            Integer num2){
        
        return num1 + num2;
    }
    
    Integer somar(Integer num1, Integer num2, Integer num3){
        
        return num1 + num2 + num3;
    }
}
